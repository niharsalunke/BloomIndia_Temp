          <div class = "lead">
         <p> Leukemia which is commonly known as blood cancer is the cancer of the blood and blood-forming tissues, including bone marrow.
           Leukemia begins in the bone marrow and results in the production of a high amount of abnormal blood cells.
            These blood cells are not fully developed and abnormal and are called blasts. With time the normal cells die out and the abnormal cells or blasts outnumber the normal blood cells. 
          This leads to symptoms such as fever, tiredness, bruises on the skin, susceptibility to infection.</p>
          <img class="blogimage col-xs-12 col-md-6" data-src="holder.js/500x500/auto" alt="500x500" src= "https://media-exp1.licdn.com/dms/image/C5612AQFD9wQVYsz1kQ/article-inline_image-shrink_1000_1488/0?e=1601510400&v=beta&t=WpjUGD9MZfXrbUJC5NIlJHs_bmuPPoWnneDeiwDCqSE" data-holder-rendered="true" style = "margin-right: 15px;margin-bottom: 20px; margin-left: -15px;">
          <p>
          Leukemia can primarily be divided into acute and chronic.
          </p>
          <b>Acute leukemia </b>
           <p>
           It is the most common form of leukemia in children. In the body of a person suffering from acute leukemia, there is a rapid increase in the number of immature blood cells leading to crowding of such cells. 
           This results in the inability of the marrow to produce healthy blood cells leading to low hemoglobin and platelets.
            These cells spill over into the bloodstream and spread into other organs, therefore acute leukemia should be treated immediately. 
           </p>
            <b>Chronic leukemia</b>
            <p>
            In a person suffering from chronic leukemia, there is a buildup of mature and irregular white blood cells that occurs over the time of months or years. 
            This excess of irregular WBC results from the rate of production which is higher than normal. Whereas acute leukemia must be treated immediately, chronic forms are sometimes monitored for some time before treatment to ensure maximum effectiveness of therapy.
             Chronic leukemia mostly occurs in older people but can occur in any age group.
            </p>
            <p>
             According to the kind of blood cell affected leukemia is divided into lymphocytic leukemia and myelogenous leukemia.
            </p>
             <b>Lymphocytic or Lymphoblastic Leukemia</b>
             <p>
             In lymphocytic leukemia, cancerous changes occur in bone marrow cells that are responsible for lymphocyte formation. A lymphocyte is a subtype of WBC in the immune system.
              Most lymphocytic leukemias involve a specific lymphocytic subtype called B cell.
            </p>             
              <b>Myelogenous or Myeloid Leukemia</b>
              <p>
              In myelogenous leukemia, marrow cells responsible for red blood cells, some types of WBC, and platelets formation undergo cancerous changes.
              </p>
              <p>
              Combining these four classifications, there are four major types of leukemia- Acute Lymphoblastic Leukemia, Chronic lymphoblastic leukemia, Acute Myelogenous Leukemia, and, Chronic Myelogenous Leukemia.
              </p>
              <img class="blogimage col-xs-12 col-md-6" data-src="holder.js/500x500/auto" alt="500x500" src= "https://media-exp1.licdn.com/dms/image/C5612AQFiPPF7hLX0MQ/article-inline_image-shrink_1000_1488/0?e=1601510400&v=beta&t=vnoaLM8GfMC0WwL-1aBHVK7AO5tyWms4w-is9yur7lw" data-holder-rendered="true" style = "margin-right: 15px;margin-bottom: 20px; margin-left: -15px;">
              <br>
              <b>Cause of Leukemia</b>
              <p>
              The exact cause of leukemia is unknown. It is believed that leukemia is a result of a combination of genetic and environmental factors.
               Leukemia cells are produced when regular cells acquire mutations in their DNA and grow abnormally, thus losing their typical functions.
              </p>
              <p>
               One common type of change occurring in the cell DNA is known as translocation. 
               In translocation, a portion of one chromosome breaks off and attaches to a different chromosome.
              </p>
              <p>
               In Chronic Myelogenous Leukemia a translocation occurs between the 9th and 22nd chromosomes, which lead to the formation of what is known as the Philadelphia chromosome. This creates an oncogene (cancer-promoting gene) known as BCR-ABL. 
               This change in DNA is not inherited but occurs sometime in the life of the affected individual.
              </p>
              <p>
               Most cases of leukemia are not believed to be hereditary,
                but certain genetic mutations and conditions can be passed along to offspring that increase the chances of developing leukemia. 
              </p>
              <p>
               Li-Fraumeni syndrome is a condition in which the individual inherits a mutation in tumor suppressor gene TP53.
                Individuals with this condition have an increased risk of leukemia and other types of cancer. 
              </p>
              <p>
               Other hereditary conditions that can increase the risk of developing leukemia include Down syndrome, neurofibromatosis type 1, ataxia-telangiectasia, and Noonan syndrome.
              </p>
              <p>
               Other factors that can increase your risk of developing leukemia are previous cancer treatment, exposure to certain chemicals (like benzene in gasoline), smoking, and family history.
              </p>
               <b>Symptoms</b>
               <p>
               Leukemia can be hard to spot because the signs and symptoms are common to other unrelated illnesses. Symptoms of leukemia vary depending on the type of leukemia.
               </p>
               <p>
               The common symptoms experienced by all leukemia patients are:-</p>
               <ul>
                   <li>Shortness of breath</li>
                   <li>Fevers and chills</li>
                   <li>Bone or joint pain</li>
                   <li>Fever or chills</li>
                   <li>Persistent fatigue, weakness</li>
                   <li>Frequent or severe infections</li>
                   <li>Losing weight without trying</li>
                   <li>Swollen lymph nodes, enlarged liver or spleen</li>
                   <li>Easy bleeding or bruising</li>
                   <li>Recurrent nosebleeds</li>
                   <li>Tiny red spots in your skin (petechiae)</li>
                   <li>Excessive sweating, especially at night</li>
               </ul>
               <p>If you have any persistent signs or symptoms that worry you consult a doctor.
               </p>
               <img class="blogimage col-xs-12 col-md-6" data-src="holder.js/500x500/auto" alt="500x500" src= "https://media-exp1.licdn.com/dms/image/C5612AQGcGfQq0z4Jpg/article-inline_image-shrink_1500_2232/0?e=1601510400&v=beta&t=G2BdQtjdEzj-Iwc5SIQjchfFnRvzBctpwsc6IJZd2sA" data-holder-rendered="true" style = "margin-right: 15px;margin-bottom: 20px; margin-left: -15px;">
                <p>Leukemia shows no obvious symptoms early in the disease, leukemia may be diagnosed incidentally during a physical exam or as a result of routine blood testing. Doctors may find chronic leukemia in a routine blood test before symptoms begin. If this happens, or if you have signs or symptoms that suggest leukemia, you may undergo the following diagnostic exams:</p>
                <b>Physical examination</b>
                <p>The doctor will look for physical symptoms of leukemia such as pale skin caused due to anemia, bruises, red spots, swelling of lymph nodes, and enlargement of liver and spleen.</p>
                <b>Blood test</b>
                <p>A blood test is carried out to check if there is an abnormal level of red blood cells, white blood cells, or platelets. </p>
                <b>Bone marrow test</b>
                <p>A sample of bone marrow is removed from the hip bone using a long thin needle and sent to a laboratory, where it is tested for leukemic cells, DNA markers, and chromosome changes in the bone marrow., . This also helps in deciding the type of treatment based on the test outcomes.

                The treatment for leukemia depends on factors like age, overall health condition, the type of leukemia, the chromosomal abnormalities found in leukemia cells, and bone marrow.</p><br><br>
                <p>Common treatments used to fight leukemia include:</p>
                <ul>
                    <li><h4>Chemotherapy.</h4><p> Chemotherapy is the major form of treatment for leukemia. This drug treatment uses chemicals to kill leukemia cells. Depending on the type of leukemia the patient may receive a single drug or a combination of drugs.</p></li>
                    <li><h4>Targeted therapy.</h4><p> Targeted therapy uses drugs that attack specific vulnerabilities within your cancer cells. For example, imatinib (Gleevec) stops the action of a protein within the leukemia cells of people with chronic myelogenous leukemia. This can help control the disease.</p></li>
                    <li><h4>Radiation therapy.</h4><p> Radiation therapy uses X-rays or other high-energy beams to damage leukemia cells and stop their growth. The patient may receive radiation in one specific area of your body where there is a collection of leukemia cells, or you may receive radiation over your whole body. Radiation therapy may be used to prepare for a stem cell transplant.</p></li>
                    <li><h4>Stem cell transplant.</h4><p> A stem cell transplant is a procedure to replace diseased bone marrow with healthy bone marrow. Before a stem cell transplant, high doses of chemotherapy or radiation therapy are given to destroy the diseased bone marrow. Then an infusion of blood-forming stem cells is given which helps to rebuild the bone marrow. The patient may receive stem cells from a donor, or in some cases, you may be able to use your own stem cells. A stem cell transplant is very similar to a bone marrow transplant.</p></li>
                </ul>
