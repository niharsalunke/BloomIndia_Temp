			<p><em>There is no greater joy then saving a life - Right Blood. Right Time. Right Place.</em></p> 
  <p>In 2016, the Ministry of Health and Family Welfare, India, reported a donation of&nbsp;10.9 million&nbsp;units against a requirement of&nbsp;12 million&nbsp;units. As per a 2017 media reports, India today faces a shortage of 10% relative to its blood requirements. Given that the eligible donor population of India is more than 512 million, this deficit is surprising. Let’s try to understand some of the important reasons.</p> 
  <p><strong>Lack of Awareness &amp; Myths</strong></p> 
  <p>Awareness about blood donation in India is sharply skewed. Among the prime cause for wide difference in blood donation is primarily the lack of knowledge about its simple process in the lesser-developed states and the various unfounded myths that people have harboured over the centuries. In a study conducted in 2011 reports that a mere 6% of women donate blood, mostly due to physiological problems and low hemoglobin count. Other hurdles in increasing voluntary blood donation include the fear of pain and weakness after the procedure, and illiteracy.</p> 
  <p><strong>Wastage of Blood</strong></p> 
  <p>Disparities in access of donors in different areas have led to wastage of blood stock in some parts of the country, while at the same time creating a shortage of blood in some other parts. Shelf life of collected blood is only 30-45 days, which means excess of blood needs to be soon sent to all needy areas. As per some media reports in 2017 citing health ministry data, despite shortage, India discards 1 million blood units every year. As much as 6 lakh liters of blood wasted in just last five years due to lack of coordination between blood banks and hospitals. In 2016, while 16 states and UTs faced a shortage of blood supply, 18&nbsp;had an excess of it.&nbsp;While some states, like Delhi are able to accumulate 233% extra blood than what is required, other needy states like Bihar, UP, Chattisgarh face a deficit of as much as 50%.&nbsp;</p> 
  <p><strong>Shortage of Volunteer Blood Donation</strong></p> 
  <p>Voluntary blood donation comprises about 70% of the blood demand, with the rest coming from replacement donors, whereas 62 nations in the world fulfill their blood demands through voluntary donations. Which means volunteer donation in India need to be increased. The number of voluntary blood donors increased from 54.4% in 2006-2007 to 83.1% in 2011-2012. The shortfall has also gone down from 17% to the present 10% of the blood requirement. Credit for this can be attributed to the many NGOs and blood bank organisations that have been operating in India, trying to sensitize the public of the benefits of blood donation.</p> 
  <p><strong>How Bloom India helps here?</strong></p> 
  <p>Bloom India tries to address some of the above mentioned issues by providing a platform where volunteer Blood Donors can be connected with needy Donees in short time in the vicinity of their city, state and country. It is available as a mobile app and website. </p> 
  <p>Donors Can pledge to donate blood. Donees can search for donors and reach out to them.&nbsp;Donors get rewarded with discount coupons which they can redeem at partner stores.</p> 
