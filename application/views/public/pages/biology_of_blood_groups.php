				<p class="lead">RBC has protein molecules called antigens and plasma has protein molecules called antibodies.</p>
				<p class="lead">Antigens are on the surface of RBCs and can be of A, B, O and AB Types. Antigens give our blood type.</p>
				<p class="lead">The antibody in the plasma has to compatible with the antigen in the RBC.</p>
				<p class="lead">4&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Main Blood Groups :</p>
