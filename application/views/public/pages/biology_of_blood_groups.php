				<p class="lead">Blood Group O – There are no RBC antigens and there are both anti-A and anti-B antibodies.</p>
				<p class="lead">Blood group AB – Both RBC A &amp; B antigens are there and no antibodies.</p>
				<p class="lead">The positivity or negativity of Blood Group is determined by RH Factor which is another Protein on the surface of RBC. If the protein is there, RH Factor is considered as Positive and absence as Negative.&nbsp;</p>
				<p class="lead">Blood group O is more common.</p>
				<p class="lead">&nbsp;If someone is having B blood group and he is given group A blood, their anti-A antibodies will attack the group A cells.</p>
