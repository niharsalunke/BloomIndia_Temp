			<p>Bombay blood group is the rarest of rare blood groups.</p> 
  <h3>HISTORY</h3> 
  <p>In 1952, Dr Y.M. Bhende, C.K. Deshpande and H.M. Bhatia of the Seth Gordhandas Sunderdas Medical College, Mumbai came across an injured&nbsp;railway worker and stab wound victim who needed blood tranfusion. They tried blood of 160 donors of other known Blood groups like A, B, O and AB. During tests they found that the moment the patient's blood was mixed with any other blood type it coagulated or clumped up. The patient's blood group was not compatible with any of the donor's blood groups</p> 
  <p>&nbsp;Finally they found a possible donor, a resident of Bombay whose blood type matched the blood of both railway worker and stab victim.</p> 
  <p>This new blood type was named by Dr Bhende as Bombay Blood Group. Medically this blood group is called HH Blood Group.</p> 
  <h3>BIOLOGY&nbsp;</h3> 
  <p>Blood consists of different kinds of cells like RBC( Red Blood Cell ) and WBC( White Blood Cell). RBCs float in a fluid called Plasma. RBC carry on their surface markers( antigens) with which the Plasma interacts. These antigens are A, B, O, AB etc. When Blood transfusion happens, the blood type of the donor and plasma of the recipient should be compatible else blood can coagulate causing dangerous situation.</p> 
  <p>The Bombay Blood Group ( HH group ) can accept blood of HH group making it very rare group.</p> 
  <h3>WHY ARE THESE PEOPLE RARE&nbsp;</h3> 
  <p>This is because of extensive inbreeding between consanguineous marriages.&nbsp;We can only guess that the ancestry of Bombay Blood group people would be same.&nbsp;</p> 
  <h3>STATISTICS</h3> 
  <ul> 
   <li>1 in 10,000 Indians have this blood group.</li> 
   <li>1 in a million Europeans have Bombay Blood Group</li> 
   <li>This phenotype is more common in the states of Western and Southern India.</li> 
   <li>Till 1988, 179 cases were recorded.</li> 
   <li>It is also found in multiple races of the world like Caucasians, Japanese, etc</li> 
