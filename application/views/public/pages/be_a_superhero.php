			<h2>Donate blood. Donate now. Donate often.</h2> 
  <p><strong>T</strong>here was a time when getting bitten by a spider, being son of a Odin or being an alien from Krypton made you a superhero. But times have changed, and now only a prick of a needle and a few minutes of your time can do it. A single bottle of donated blood can save up to 3 lives, and if you’ve ever saved that many lives, then you are no less than a superhero! After all, it takes courage and real heroism to face that fat needle and give selflessly.</p> 
  <p><strong>Why donate blood?</strong></p> 
  <p><strong>A</strong>s Winston Churchill once said – “I have nothing to offer but&nbsp;blood, toil, tears and sweat”. Blood&nbsp;is the most precious gift that anyone can give to another person. According to the&nbsp;American Red Cross, one unit of blood donation can save as many as 3 lives. Reasons for donating Blood could be:</p> 
  <p> For the Joy of Saving Human Lives- Be a hero by donating Blood.</p> 
  <p> Scarcity of Blood: Blood donation is a major concern to the society as donated blood is lifesaving for individuals who need it. Many patients die because they are not able to cope with need of blood.</p> 
  <p> Others - General health check-up, reduce heart diseases, Stimulates new blood cells</p> 
  <p><strong>Who can donate blood?</strong></p> 
  <p><strong>A</strong>ccording to guidelines by the Ministry of Health there are 6 important parameters that determine whether you are eligible to donate blood. These are:</p> 
  <p>-&nbsp;Minimum prescribed weight = 50 kg &amp; Age = 18-60 yrs</p> 
  <p>-&nbsp;Normal body temperature. Oral temperature &lt;= 37.5 degree celsius.</p> 
  <p>-&nbsp;No communicable diseases- HIV, ailments like cancer or tuberculosis etc.</p> 
  <p>-&nbsp;The minimum prescribed haemoglobin level for blood donation is 12.5 g/dL.</p> 
  <p>-&nbsp;Pulse rate between 50 to 100 without irregularities.</p> 
  <p>-&nbsp;Diastolic BP b/w 50-100 mm Hg, and Systolic BP b/w 100-180 mm Hg.</p> 
  <p><strong>How long does the process of donation take?</strong></p> 
  <p>Only 350 ml of blood is taken at the time of donation. The actual bleeding time is about 5-6 minutes. The whole process takes about 30 minutes. There will be a medical check-up before this and you will be advised some rest (for 5-10 minutes) and given some refreshment after donation.</p> 
  <p><strong>Who should not donate blood?</strong></p> 
  <p><strong>T</strong>he following categories/conditions of people should avoid donating blood:</p> 
  <p>-&nbsp;No medicine in the last 48 hours and not on certain specified medication.</p> 
  <p>-&nbsp;Avoid smoking on the day before donating.</p> 
  <p>-&nbsp;Pregnant or lactating women, or those who have recently had an abortion.</p> 
  <p>-&nbsp;Persons with multiple sexual partners or those who are addicted to drugs.</p> 
  <p>-&nbsp;Persons who have undergone surgery in the previous six months.</p> 
  <p>-&nbsp;Persons who have consumed alcohol in the 48 hours prior to donation.</p> 
  <p>-&nbsp;Women should avoid donation during their menstruating period.</p> 
  <p><strong>Tips on Blood Donating</strong></p> 
  <p>-&nbsp;Please have a good meal at least 3 hours before donating blood.</p> 
  <p>-&nbsp;Please accept snacks offered after donation. It's recommended to have a good meal later.</p> 
  <p><strong>Donate Blood ?</strong></p> 
  <p><strong>I</strong>f you are considering donating blood to save one of the million patients battling for their life, here are a few things you should know first.</p> 
  <p><strong>1. Donating blood is completely safe: </strong>New and sterile equipment is used for each donor</p> 
  <p><strong>2. You can donate whole blood or its specific components</strong></p> 
  <p><strong>3. Only type O negative blood can be transfused to blood types: </strong>Only 6.6% of the world population has this blood type.</p> 
  <p><strong>5. You have to wait for 56 days to donate again: </strong>Donors who donate whole blood have to weight for a minimum of 8 weeks before donating blood again. Additionally, if you are donating red blood cells, you will have to wait for 16 weeks for another donation. In terms of volume the loss is corrected in 24-48 hours by the body. The red cell count is corrected in about 56 days. Hence, the minimum time advised between two donations is 3 months.</p> 
