  <h3><strong>Accidents Do Not Arrive With A Bell !!</strong></h3> 
  <p>Recently, one of our close friends Arjun (name changed) met with a road accident while returning back from office. A speeding car had lost its control and in turn hit his two-wheelers from behind. Arjun was admitted to the ICU at the nearby hospital. Luckily, the hospital personal could contact Arjun's spouse Kavita's (name changed) immediately. When Kavita rushed to the hospital, she was advised to arrange for three units of blood from the nearest blood bank. The referred blood bank, however, asked Kavita to bring in three donors to replace each of the three units of blood that she requested for. It took Kavita about 24 hours to arrange for three donors to replenish the blood units she had collected from the blood bank. Kavita was lucky, as she was connected to her network of friends to find replacement donors quickly without much hassle or delay.</p> 
  <h3><strong>Not All Experiences Are Pleasant: Replacement Donors</strong></h3> 
  <p>However, not all patients who needed blood were fortunate. In the case of Mr. Sunil, who has been making frequent visits to the hospital ever since his mother suffered from multiple myeloma, a cancer formed from malignant plasma cells in the blood. His mother needed blood transfusions every three months. While some hospitals gave Mr. Sunil blood without insisting on replacement donors for the first few transfusions, they eventually asked him to bring in donors for the successive transfusions. He recalls being exhausted in his search for blood donors, which remained persistent till the last day of his mother’s life.</p> 
  <p>The two instances narrated as above are examples of repetitive unpleasant experiences of most of the patients who needed blood urgently. The families of patients go through an enormous amount of mental stresses, anxieties as well as physical exhaustion to arrange for the blood in emergency needs.</p> 
  <h3><strong>Blood Donation Experiences</strong></h3> 
  <p>A close study of the experience of the patients who needed blood and the mechanism that runs at the hospitals/blood banks reveals the following:</p> 
  <ul> 
   <li>Many of the blood bank/hospital insist on bringing in replacement donors from from family members or friends for every unit of blood the patient needs.</li> 
   <li>In situations, where family member is unable to find a donor, the replacement donor is contacted randomly. In such cases, there is not enough time to verify donor’s medical history. Therefore, the quality of the blood is compromised as it carries a higher risk of carrying transfusion-transmissible diseases.</li> 
   <li>Many blood banks overcharge for the blood units and its components or for the tests that they never run. A few blood banks do not even have the mandatory Nucleic Acid Test (NAT) facility that is required to detect the presence of recent HIV infections in the blood.</li> 
   <li>Sometimes, collected blood is thrown off and wasted either due to higher storage cost or expiry of its limited shelf-life or because of an absence of a blood-sharing network amongst the blood banks and hospitals</li> 
  </ul> 
  <h3><strong>What Can We Do ?</strong></h3> 
  <p>How could we establish an efficient and <a href="http://www.ksaps.gov.in/blood_safty.html" target="_blank" rel="nofollow noopener">robust framework</a> to fulfill the needs of patients requiring blood ?&nbsp;The key areas and action items to address the issues could be the following:</p> 
  <h3>1.&nbsp;&nbsp;Encourage Voluntary Donation</h3> 
  <p>The first and foremost would be to <a href="http://www.who.int/bloodsafety/publications/9789241599696/en/" target="_blank" rel="nofollow noopener">eliminate replacement donors</a>. This is in accordance with the <a href="http://www.who.int/bloodsafety/voluntary_donation/en/" target="_blank" rel="nofollow noopener">World Health Organisation</a> (WHO) guidelines that all blood collection should be through voluntary non-remunerative donations.</p> 
  <ul> 
   <li>A safe and sustainable blood supply can only be met through voluntary non-remunerated donation</li> 
  </ul> 
  <h3>2. Increase Community Awareness</h3> 
  <p>Firstly, <a href="http://drugs.kar.nic.in/node/232.html" target="_blank" rel="nofollow noopener">more awareness campaigns</a> throughout the year to educate the <a href="https://www.organicfacts.net/health-benefits/other/blood-donation.html" target="_blank" rel="nofollow noopener">benefits of blood donation</a> and community education programme to eradicate the <a href="http://indiatoday.intoday.in/education/story/world-blood-donor-day/1/691349.html" target="_blank" rel="nofollow noopener">myths</a> and fears that prevent donating blood are needed. Secondly, it is important to organize regular blood camps in diverse locations to reach out to all donor population.</p> 
  <ul> 
   <li>Every eligible blood donor needs to be educated and made aware of his social responsibility to donate blood</li> 
  </ul> 
  <h3>3.&nbsp;&nbsp;&nbsp;Efficient Blood Distribution Network</h3> 
  <p>An efficient and robust blood distribution network must exist to facilitate efficient blood delivery from blood banks/hospitals to <a href="http://indianexpress.com/article/cities/mumbai/maharashtra-govt-to-set-up-207-blood-banks-in-rural-areas-2775414/" target="_blank" rel="nofollow noopener">every remote part of India</a> at affordable and reasonable <a href="http://www.business-standard.com/article/current-affairs/maha-govt-launches-unique-blood-on-call-scheme-114010701199_1.html" target="_blank" rel="nofollow noopener">cost</a>. This is important considering the fact that a staggering 70% of donors’ lives in rural areas, and either have limited access or no access to blood banks/hospitals</p> 
  <ul> 
   <li>All blood banks and rural hospitals need to be integrated to an efficient blood distribution network</li> 
  </ul> 
  <h3>4.&nbsp;Reaching Out to Corporates and Organizations</h3> 
  <p>Tie-ups with the organizations on their Corporate Social Responsibility (CSR) program. Under “New Companies Act 2013”, few select companies spend at least 2% of its three-year average annual net profits on social cause.</p> 
  <ul> 
   <li>On world blood day, 13th June 2017, UBER - the largest taxi company, had partnered with Fortis Hospitals Bangalore, to offer free rides to blood-donors from anywhere in Bangalore to Fortis hospitals.</li> 
  </ul> 
  <h3>5.&nbsp;Service and Dedication to a Cause</h3> 
  <p><a href="http://www.mahasbtc.com/blood-donor-motivators" target="_blank" rel="nofollow noopener">Blood donation</a> is a pleasant and happy experience filled with <a href="http://www.thehindu.com/news/cities/Mangalore/They-take-pride-in-donating-blood/article14423885.ece" target="_blank" rel="nofollow noopener">pride</a> and satisfaction of <a href="http://indiatoday.intoday.in/story/small-town-becomes-pride-of-bhatinda-district-for-its-record-in-blood-donation/1/281870.html" target="_blank" rel="nofollow noopener">saving</a> precious lives.&nbsp;</p> 
  <ul> 
   <li>Anyone between the 18 to 65 years of age, over 45 kg weight and in good health can donate blood</li> 
  </ul>
